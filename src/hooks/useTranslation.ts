export function useTranslation() {
  // Initialize locale from localStorage or default to Arabic
  const [locale, setLocale] = useState<Locale>(() => {
    if (typeof window !== 'undefined') {
      const savedLocale = localStorage.getItem('preferred-language') as Locale;
      return savedLocale || 'ar';
    }
    return 'ar';
  });

  const t = (key: string, params?: Record<string, string | number>): string => {
    // @ts-expect-error - We know these translations exist
    let translation = translations[locale][key] || key;
    
    // Handle interpolation if params are provided
    if (params) {
      Object.entries(params).forEach(([paramKey, value]) => {
        translation = translation.replace(`{${paramKey}}`, String(value));
      });
    }
    
    return translation;
  };

  const changeLocale = (newLocale: Locale) => {
    setLocale(newLocale);
    if (typeof window !== 'undefined') {
      localStorage.setItem('preferred-language', newLocale);
    }
  };

  return {
    t,
    locale,
    changeLocale,
  };
}


