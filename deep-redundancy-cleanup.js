function scanForRemainingIssues() {
  console.log('\nüîç Scanning for remaining redundancy issues...\n');
  
  // Check for any remaining v1/old/backup patterns
  const suspiciousPatterns = [
    /.*-v1\.(tsx?|jsx?)$/,
    /.*\.old\.(tsx?|jsx?)$/,
    /.*\.backup\.(tsx?|jsx?)$/,
    /.*-old\.(tsx?|jsx?)$/,
    /.*-backup\.(tsx?|jsx?)$/,
    /.*-copy\.(tsx?|jsx?)$/
  ];

  const suspiciousFiles = [];
  
  function scanDirectory(dirPath, relativePath = '') {
    try {
      const items = fs.readdirSync(dirPath);
      items.forEach(item => {
        const fullPath = path.join(dirPath, item);
        const itemRelativePath = path.join(relativePath, item);
        
        if (fs.statSync(fullPath).isDirectory()) {
          if (!['node_modules', '.git', '.next'].includes(item)) {
            scanDirectory(fullPath, itemRelativePath);
          }
        } else {
          suspiciousPatterns.forEach(pattern => {
            if (pattern.test(item)) {
              suspiciousFiles.push(itemRelativePath);
            }
          });
        }
      });
    } catch (error) {
      // Skip directories we can't read
    }
  }

  scanDirectory(path.join(__dirname, 'src'));
  
  if (suspiciousFiles.length > 0) {
    console.log('‚ö†Ô∏è Remaining suspicious files found:');
    suspiciousFiles.forEach(file => {
      console.log(`   - ${file}`);
    });
    console.log('\nüí° Consider reviewing these files for potential removal');
  } else {
    console.log('‚úÖ No remaining suspicious patterns found');
  }
}

