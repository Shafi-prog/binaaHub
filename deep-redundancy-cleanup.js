function removeRedundantFiles() {
  console.log('üßπ Starting deep redundancy cleanup...\n');
  
  let removedCount = 0;
  let skippedCount = 0;
  let totalSizeFreed = 0;

  console.log('üìÑ Removing functional duplicates...\n');
  
  redundancyCleanup.functionalDuplicates.forEach(({ keep, remove }) => {
    const removePath = path.join(__dirname, 'src', remove);
    const keepPath = path.join(__dirname, 'src', keep);
    
    try {
      if (fs.existsSync(removePath)) {
        const stats = fs.statSync(removePath);
        const size = stats.size;
        
        // Verify the file we're keeping exists
        if (fs.existsSync(keepPath)) {
          fs.unlinkSync(removePath);
          console.log(`   ‚úÖ Removed: ${remove} (keeping ${keep}) - ${(size / 1024).toFixed(2)} KB`);
          removedCount++;
          totalSizeFreed += size;
        } else {
          console.log(`   ‚ö†Ô∏è  Cannot remove ${remove} - keep file ${keep} doesn't exist`);
          skippedCount++;
        }
      } else {
        console.log(`   ‚ö™ Not found: ${remove}`);
        skippedCount++;
      }
    } catch (error) {
      console.log(`   ‚ùå Failed to remove ${remove}: ${error.message}`);
      skippedCount++;
    }
  });

  console.log(`\nüìä Deep Cleanup Summary:`);
  console.log(`‚úÖ Redundant files removed: ${removedCount}`);
  console.log(`‚ö™ Files skipped: ${skippedCount}`);
  console.log(`üíæ Space freed: ${(totalSizeFreed / 1024 / 1024).toFixed(2)} MB`);
}

